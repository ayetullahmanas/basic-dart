// 1.9.5 Eerişebilirlik (public ve private)

/*
- Erişebilirlik kavramları sınıfa ait bir öğenin (değişken, fonksiyon) sınıf dışından
  erişilip erişilemeyeceğini belirliyordu.

- Dard dilinde diğer dillerden farklı olarak 'protected' kavramı bulunmaz.
  Sadece public ve private kavramları bulunur


- public: o sınıf dışındaki sınıflardan veya fonksiyonlardan erişilebilen öğeler
  (değişken veya fonksiyonlar) public olarak tanımlanır.

- private: sınıf dışından, yani başka sınıflar veya fonksiyonlar tarafından
  erişilemeyen öğeler private öğelerdir.

- dart dilinde private kavramı için değişkenin başına '_' işareti getirilir. 

 */